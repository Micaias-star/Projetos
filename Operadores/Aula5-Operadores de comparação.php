<?php

$a = 30.0;

$b = 55;

var_dump($a > $b ); // Operador "maior que"

 echo "<br>";

var_dump($a < $b); // operador "menor que"

echo "<br>";

var_dump($a == $b); // O operador  = é de atribuição. já o que é de == é de comparação.

echo "<br>";

var_dump($a === $b); // O operador === compara o valor da variável e o tipo delas.
 
 echo "<br>";

var_dump($a != $b);  //operador que pergunta de as variáveis são diferentes no que dis respeito ao valor

echo "<br>";

var_dump($a !== $b); //operador que pergunta de as variáveis são diferentes no que dis respeito ao valor e ao tipo


?>